package com.example.agamymaxb.pojo;

public class Brand {

    private int id;
    private int image;

    public Brand(int image) {
        this.image = image;
    }

    public int getImage() {
        return image;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setImage(int image) {
        this.image = image;
    }


}
